"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
String são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Hygor Rocha')
print(1, 'Hygor "Rocha"')

# Aspas duplas
print("Hygor Rocha")
print(2, "Hygor 'Rocha'")

# Escape
print("Hygor \"Rocha\"")

# r
print(r"Hygor \"Rocha\"")