# Váriaveis são usadas para salvar algo na memória do computador.
# PEP8: inicia variáveis com letras minúsculas, pode usar
# números e underline _.
# O sinal de = é o operador de atribuição. Ele é usado para
# atribuir um valor a um nome (variavel).
# Uso: nome variavel =  expressao
nome_completo = 'Hygor Melo Rocha'
soma_dois_mais_dois = 2 + 2
print(nome_completo, soma_dois_mais_dois)

nome = 'Hygor'
idade = 27
